export type ThreadMessageView = {
  id: string;
  body: string;
  imageUrl: string | null;
  senderId: string;
  createdAt: string;
  editedAt: string | null;
  unsentAt: string | null;
  isMine: boolean;
  isRead: boolean;
  likeCount: number;
  likedByMe: boolean;
};

export function canEditMessage(message: { isMine: boolean; unsentAt: string | null; createdAt: string }) {
  if (!message.isMine || message.unsentAt) return false;
  return Date.now() - new Date(message.createdAt).getTime() <= MESSAGE_EDIT_WINDOW_MS;
}

export function canUnsendMessage(message: { isMine: boolean; unsentAt: string | null }) {
  return message.isMine && !message.unsentAt;
}
